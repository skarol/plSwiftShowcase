import Foundation
import CoreGraphics
import DesignSystemUtils

public extension DSTokens.Spaces {
    /// The Extra Small Space size.
    /// Equal to ``4``.
    static let xs: CGFloat = 4
    /// The Small Space size.
    /// Equal to ``8``.
    static let s: CGFloat = 8
    /// The Medium Space size.
    /// Equal to ``12``.
    static let m: CGFloat = 12
    /// The Large Space size.
    /// Equal to ``24``.
    static let l: CGFloat = 24
    /// The Extra Large Space size.
    /// Equal to ``48``.
    static let xl: CGFloat = 48
    /// The Double Extra Large Space size.
    /// Equal to ``64``.
    static let xxl: CGFloat = 64
}
